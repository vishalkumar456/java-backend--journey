package java_day_1;

public class basics_of_escape_sequences {

	public static void main(String[] args) {
	System.out.println("1.Understanding of double quote character_");
	System.out.println("MY NAME IS :\"VISHAL\" ");
	System.out.println("2.Understanding of single quote_");
	System.out.println("MY NAME IS :'VISHAL'");
	System.out.println("\'vishal\'");
	System.out.println("3.Understanding of printing of back slash");
	System.out.println("\\vishal\\");
	System.out.println("4.Understanding of line changing_");
	System.out.println("MY NAME IS\nVISHAL");
	System.out.println("5.Understanding the use of Tab_");
	System.out.println("VISHAL\tKUMAR");
	System.out.println("6.Understanding of backspace_");
	System.out.println("vishal\bkumar");

	}

}
